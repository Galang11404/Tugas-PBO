print("Aplikasi menghitung luas  dan volume balok")

# Atur nilai variabel
panjang = 10
tinggi = 5
lebar = 6

# Rumus 
luas = (2 * panjang * lebar) + (2 * panjang * tinggi) + (2 * lebar * tinggi)
volume = (panjang * lebar * tinggi)

# Output
print("luas =",luas)
print("volume =",volume)
