print("Aplikasi menghitung luas dan volume Selinder")

# Atur nilai variabel
jari_jari = 15
tinggi = 35
phi = 3.14

# Rumus 
Luas_selimut = 2 * phi * jari_jari * tinggi
Luas_permukaan = (2 * phi * jari_jari * tinggi) + (2 * phi * jari_jari **2)
volume = phi * jari_jari **2 * tinggi 

#Output
print("Luas_selimut = ",Luas_selimut)
print("Luas_permukaan = ",Luas_permukaan)
print("Volume = ",volume)
