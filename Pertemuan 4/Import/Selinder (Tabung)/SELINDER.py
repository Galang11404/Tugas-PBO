
def hitung_luas_selimut(jari_jari,t):
    phi = 3.14   
    return round (2 * phi * jari_jari * t)
   
def hitung_luas_permukaan(jari_jari,t):
    phi = 3.14  
    return round ((2 * phi * jari_jari * t) + (2 * phi * jari_jari **2))
    
def hitung_volume(jari_jari,t):
    phi = 3.14  
    return round(phi * jari_jari **2 * t)
   